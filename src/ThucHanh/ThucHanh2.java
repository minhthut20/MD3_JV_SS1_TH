package ThucHanh;

public class ThucHanh2 {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        boolean b = true;
        char c = 'a';
        String s = "Yên Bái";
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
